# Accessing Dictionary using for loop

# Initial dictionary
stu = {101: 'imran', 102: 'mamun', 103: 'samin'}

# Accessing Single Value
print(stu[101])  # Print value corresponding to key 101

# Display Keys
for k in stu:
    print(k)  # Print each key in the dictionary

# Display Values
for k in stu:
    print(stu[k])  # Print each value corresponding to its key in the dictionary
