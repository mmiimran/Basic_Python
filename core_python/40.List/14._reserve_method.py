# reverse() Method
# Creating a list 'a' containing integers and a string
a = [10, 20, 30, 10, 90, 'Imranshows']

# Printing the list 'a' before reversing
print("Before Reverse:", a)

# Reversing the elements of the list 'a' in-place
a.reverse()

# Printing the list 'a' after reversing
print("After Reverse:", a)

# Printing each element of the modified list 'a'
for element in a:
    print(element)
