# Append Method

# Creating a list named 'a' containing different types of elements
a = [10, 20, -50, 21.3, 'Imranshow']

print("Before Appending:")
for element in a:
    print(element)

# Appending an element to the list 'a'
a.append(100)

print()
print("After Appending")
for element in a:
    print(element)
