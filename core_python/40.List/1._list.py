# Accessing elements of a list using indices

# Creating a list named 'a' containing different types of elements
a = [10, 20, -50, 21.3, 'Imranshows']

# Accessing elements of the list using positive indices
print(a[0])    # Prints the first element of the list
print(a[1])    # Prints the second element of the list
print(a[2])    # Prints the third element of the list
print(a[3])    # Prints the fourth element of the list
print(a[4])    # Prints the fifth element of the list
print()        # Empty line for separation

# Accessing elements of the list using negative indices
print(a[-1])   # Prints the last element of the list
print(a[-2])   # Prints the second last element of the list
print(a[-3])   # Prints the third last element of the list
print(a[-4])   # Prints the fourth last element of the list
print(a[-5])   # Prints the fifth last element of the list
