# sort() Method
# Creating a list 'a' containing integers
a = [10, 5, 90, 10, 30]

# Printing the list 'a' before sorting
print("Before Sort", a)

# Sorting the list 'a' in ascending order
a.sort()

# Printing the list 'a' after sorting
print("After Sort", a)

# Printing each element of the sorted list 'a'
for element in a:
    print(element)
