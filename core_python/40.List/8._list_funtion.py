# Creating Empty List using list Function
a = list()  # Creating an empty list using the list() function
print(a)    # Printing the empty list
print(type(a))  # Printing the type of the list

# Creating list using list Function and range function
b = list(range(0, 5))  # Creating a list using the list() function and the range() function
print(b)    # Printing the created list
print(type(b))  # Printing the type of the list
