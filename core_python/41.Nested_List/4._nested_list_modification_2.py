# Nested List - Modification

# Original nested list
a = [[10, 20, 30], [40, 50, 60]]

# Print the original list before modification
print("Before Modification A:", a)
print()

# Modify elements of the nested list
a[0][1] = 2   # Modify the second element of the first inner list
a[1][2] = 6   # Modify the third element of the second inner list

# Print the list after modification
print("After Modification A:", a)
