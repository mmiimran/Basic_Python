# Nested List
a = [[10, 20, 30], [40, 50, 60]]

# Print the entire nested list
print("A:", a)

# Print the first sublist
print("a[0]:", a[0])

# Print the second sublist
print("a[1]:", a[1])
print()

# Access individual elements using indices
print(a[0][0])  # Accessing element at row 0, column 0
print(a[0][1])  # Accessing element at row 0, column 1
print(a[0][2])  # Accessing element at row 0, column 2
print(a[1][0])  # Accessing element at row 1, column 0
print(a[1][1])  # Accessing element at row 1, column 1
print(a[1][2])  # Accessing element at row 1, column 2
