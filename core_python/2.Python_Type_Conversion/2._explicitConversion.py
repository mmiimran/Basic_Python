# Integer Division
# Dividing integer 'a' by integer 'b' results in a floating-point value
a = 5
b = 2
value = a / b
print(type(value))

# Converting Float to Integer
# Converting the floating-point value 'value' to an integer
int_value = int(value)
print(int_value)
print(type(int_value))

