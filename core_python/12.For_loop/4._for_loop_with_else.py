# For Loop with Else
# Defining a string variable 'st'
st = "Code2learn"

# Iterating over each character in the string 'st'
for i in st:
    # Printing each character
    print(i)
else:
    # Executing the else block after the loop finishes
    print("Else Part Executed")

# Printing a message after the loop finishes
print("Rest of the Code")
