# For Loop with Range
# Defining a string variable 'st'
st = "Code2learn"

# Determining the length of the string 'st'
n = len(st)

# Iterating over each index in the range of the length of the string 'st'
for i in range(n):
    # Printing the current index and the corresponding character in the string
    print(i, "=", st[i])

# Printing a message after the loop finishes
print("Rest of the Code")
