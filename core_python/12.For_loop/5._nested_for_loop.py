# Nested For Loop
# Iterating over the range from 0 to 2 (exclusive)
for i in range(3):
    # Printing the outer loop index
    print("Outer Loop", i)
    # Iterating over the range from 0 to 4 (exclusive)
    for j in range(5):
        # Printing the inner loop index
        print("Inner Loop", j)

# Printing a message after both loops finish executing
print("Rest of the Code")
