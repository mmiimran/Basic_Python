# Bitwise_Operator: This script demonstrates the use of bitwise operators in Python.

# Assigning integer values to variables a and b
a = 10				#	0000 1010
b = 15				#	0000 1111	

# Bitwise NOT operator (~)
print('~a = ', ~a)

# Bitwise AND operator (&)
print('a&b = ', a&b)

# Bitwise OR operator (|)
print('a|b = ', a|b)

# Bitwise XOR operator (^)
print('a^b = ', a^b)

# Left shift operator (<<)
print('a<<2 = ', a<<2)

# Right shift operator (>>)
print('a>>2 = ', a>>2)
