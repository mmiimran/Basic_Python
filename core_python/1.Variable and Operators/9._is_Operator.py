# is operator
# Assigning the integer value 10 to variable a
a = 10
# Assigning the integer value 10 to variable b
b = 10
# Checking if the objects referenced by a and b are the same
print(a is b)

# Assigning the integer value 10 to variable a
a = 10
# Assigning the string value '10' to variable b
b = '10'
# Checking if the objects referenced by a and b are the same
print(a is b)
