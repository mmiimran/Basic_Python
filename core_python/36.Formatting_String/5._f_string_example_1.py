# Integer
print("******** Integer *********")  # Printing section header for integers
a = 10  # Assigning the value 10 to variable 'a'
b = 20  # Assigning the value 20 to variable 'b'
print(f"{a}")  # Printing 'a' using f-string format
print(f"{a} {b}")  # Printing 'a' and 'b' using f-string format
print(f"{b} {a}")  # Printing 'b' and 'a' using f-string format

# Float
print("******** Float *********")  # Printing section header for floats
c = 10.56  # Assigning the value 10.56 to variable 'c'
d = 20.42  # Assigning the value 20.42 to variable 'd'
print(f"{c}")  # Printing 'c' using f-string format
print(f"{c} {d}")  # Printing 'c' and 'd' using f-string format
print(f"{d} {c}")  # Printing 'd' and 'c' using f-string format

# String
print("******** String *********")  # Printing section header for strings
f_name = "Imran"  # Assigning the value "Imran" to variable 'f_name'
l_name = "Shows"  # Assigning the value "Shows" to variable 'l_name'
print(f"{f_name}")  # Printing 'f_name' using f-string format
print(f"{f_name} {l_name}")  # Printing 'f_name' and 'l_name' using f-string format
print(f"{l_name} {f_name}")  # Printing 'l_name' and 'f_name' using f-string format

# Integer and String
name = "Imran Shows"  # Assigning the value "Imran Shows" to variable 'name'
age = 10  # Assigning the value 10 to variable 'age'
print(f"Hello My Name is {name}")  # Printing a formatted string with 'name'
print(f"{name} {age}")  # Printing 'name' and 'age' using f-string format
print(f"{age} {name}")  # Printing 'age' and 'name' using f-string format
