# Print string with double quotes
print("Welcome to Code2learn")

# Print blank line
print()

# Print string with Single quotes
print('Welcome to  Code2learn')

# Print each word in the same line because inside one print function
print("Like", "Share", "Subscribe")

# Print integer
print(10)

# Prints in separate lines
print("Welcome")
print("to")
print(" Code 2 learn")

# Print list
data = [10, 20, -50, 25.3, 'Code2learn']
print(data)

# Print with separator
print("Like", "Share", "Subscribe", sep="***")

# Print with end 'space'
print("Welcome", end="")
print("to", end="")
print("Code2learn")

# Print with end 'tab'
print("We", end="\t")
print("to", end="\t")
print("Co")

# Print variable value
a = 10
print(a)

# Print variable value with separator
x = 20
y = 30
print(x, y)
print(x, y, sep=',')

# Print with integer and string
m = 60
print("Value: ", m)

# Print with integer and string using f-string formatting
name = "Imran"
age = 35
print(f"My Name is  {name} and My age is {age}")
