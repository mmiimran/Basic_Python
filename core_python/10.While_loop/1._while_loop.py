# While Loop
# Initializing variable 'a' to 1
a = 1

# Looping while 'a' is less than or equal to 10
while a <= 10:
    # Printing the current value of 'a'
    print(a)
    # Incrementing 'a' by 1
    a += 1

# Printing a message after the loop finishes
print("Rest of the Code")

# Reinitializing variable 'a' to 2
a = 2

# Looping while 'a' is less than or equal to 20
while a <= 20:
    # Printing the current value of 'a'
    print(a)
    # Incrementing 'a' by 2
    a += 2

# Printing a message after the loop finishes
print("Rest of the Code")
