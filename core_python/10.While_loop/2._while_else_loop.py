# While Else Loop
# Initializing variable 'a' to 1
a = 1

# Looping while 'a' is less than or equal to 10
while a <= 10:
    # Printing the current value of 'a'
    print(a)
    # Incrementing 'a' by 1
    a += 1
else:
    # Executing the else block when the while condition becomes False
    print("While Condition FALSE So Else Part Executed")

# Printing a message after the loop finishes
print("Rest of the Code")
