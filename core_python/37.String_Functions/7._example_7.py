# Printing the header for the startswith function
print("****** startswith Function ******")

# Assigning the value "Hi How are you" to variable 'name'
name = "Hi How are you"
# Applying the startswith() function to check if 'name' starts with 'Hi'
str1 = name.startswith('Hi')

# Printing the original 'name' and the result of the startswith() function
print(name)
print(str1)

# Printing the header for the endswith function
print("****** endswith Function ******")

# Assigning the value "Thank you Bye" to variable 'name'
name = "Thank you Bye"
# Applying the endswith() function to check if 'name' ends with 'Bye'
str1 = name.endswith('Bye')

# Printing the original 'name' and the result of the endswith() function
print(name)
print(str1)
