# Printing the header for the upper function
print("****** Upper Function ******")

# Assigning the value "geekyshows" to variable 'name'
name = "imran"

# Applying the upper() function to convert 'name' to uppercase
str1 = name.upper()

# Printing the original 'name' and the result after applying the upper() function
print(name)
print(str1)

# Printing the header for the lower function
print("****** Lower Function ******")

# Assigning the value "GEEKYSHOWS" to variable 'name'
name = "IMRAN"

# Applying the lower() function to convert 'name' to lowercase
str1 = name.lower()

# Printing the original 'name' and the result after applying the lower() function
print(name)
print(str1)

# Printing the header for the swapcase function
