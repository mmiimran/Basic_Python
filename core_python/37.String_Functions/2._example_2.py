# Printing the header for the isupper function
print("****** isupper Function ******")

# Assigning the value "imran show" to variable 'name'
name = "IMRAN SHOW"

# Applying the isupper() function to check if all characters in 'name' are uppercase
str1 = name.isupper()

# Printing the original 'name' and the result of the isupper() function
print(name)
print(str1)

