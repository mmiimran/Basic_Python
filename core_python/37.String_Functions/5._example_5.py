# Printing the header for the lstrip function
print("****** lstrip Function ******")

# Assigning the value "   Geekyshows" to variable 'name'
name = "   Imran Show"

# Applying the lstrip() function to remove leading whitespace from 'name'
str1 = name.lstrip()

# Printing the original 'name' and the result of the lstrip() function
print(name)
print(str1)

# Printing the header for the rstrip function
print("****** rstrip Function ******")

# Assigning the value "Imran Show  " to variable 'name'
name = "Imran Show  "

# Applying the rstrip() function to remove trailing whitespace from 'name'
str1 = name.rstrip()

# Printing the original 'name' and the result of the rstrip() function
print(name)
print(str1)

# Printing the header for the strip function
print("****** strip Function ******")

# Assigning the value "   Imran Show  " to variable 'name'
name = "   Imran Show  "

# Applying the strip() function to remove leading and trailing whitespace from 'name'
str1 = name.strip()

# Printing the original 'name' and the result of the strip() function
print(name)
print(str1)
