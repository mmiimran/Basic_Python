# Nested Tuple
a = ((10, 20, 30), (40, 50, 60))
print("A:", a)
print("a[0]:", a[0])   # Accessing the first nested tuple
print("a[1]:", a[1])   # Accessing the second nested tuple
print()
print(a[0][0])         # Accessing the first element of the first nested tuple
print(a[0][1])         # Accessing the second element of the first nested tuple
print(a[0][2])         # Accessing the third element of the first nested tuple
print(a[1][0])         # Accessing the first element of the second nested tuple
print(a[1][1])         # Accessing the second element of the second nested tuple
print(a[1][2])         # Accessing the third element of the second nested tuple
