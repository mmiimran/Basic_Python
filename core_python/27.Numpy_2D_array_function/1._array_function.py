# 2D Array using Array Function Numpy
import numpy
a = numpy.array([[10, 20, 30, 40], [50, 60, 70, 80]])
print(a)
print(a.dtype)
print(a[0][0])
print(a[0][1])
print(a[0][2])
print(a[0][3])
print(a[1][0])
print(a[1][1])
print(a[1][2])
print(a[1][3])

