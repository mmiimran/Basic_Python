# If elif Statement
a = 20
b = 30

# Checking if 'a' is greater than 'b'
if a > b:
    print("a is greater than b")
# If 'a' is not greater than 'b', checking if 'a' is equal to 'b'
elif a == b:
    print("a and b are equal")
# If 'a' is neither greater than 'b' nor equal to 'b', it must be less than 'b'
elif a < b:
    print("a is less than b")


