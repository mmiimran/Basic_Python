# Importing specific functions from the 'math' module

# Importing the 'floor', 'ceil', 'sqrt', 'factorial', and 'pow' functions from the 'math' module
from math import *

# Using the imported functions
print(floor(4.5))       # Prints the floor value of 4.5
print(ceil(4.5))        # Prints the ceiling value of 4.5
print(sqrt(49))         # Prints the square root of 49
print(factorial(5))     # Prints the factorial of 5
print(pow(5, 2))        # Prints 5 raised to the power of 2
